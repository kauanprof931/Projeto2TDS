// Operadores Aritméticos
// São utilizados para realizar cálculos matemáticos

let valor1 = 10;
let valor2 = 10;

console.log("Operadores Aritméticos");
console.log("Adição: 10 + 3 = ", valor1 + valor2);
console.log("Subtração: 10 - 3 = ", valor1 - valor2);
console.log("Multiplicação: 10 * 3 = ", valor1 * valor2);
console.log("Divisão: 10 / 3 = ", valor1 / valor2);

// Operadores Comparativos
// São utilzados para realizar uma comparação e obter um valor lógico a partir dela
console.log("Operadores Comparativos");
console.log("Igualdade: 10 == 3 = ", valor1 == valor2);
console.log("Desigualdade: 10 != 3 = ", valor1 != valor2);
console.log("Maior ou igual: 10 >= 3 = ", valor1 >= valor2);
console.log("Menor ou igual: 10 <= 3 = ", valor1 <= valor2);
console.log("Maior: 10 > 3 = ", valor1 > valor2);
console.log("Menor: 10 < 3 = ", valor1 < valor2);
